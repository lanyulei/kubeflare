    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'fk_iam_external_identities_user'
    ) THEN
        DELETE FROM iam_external_identities identities
        WHERE NOT EXISTS (
            SELECT 1
            FROM iam_users users
            WHERE users.id = identities.user_id
        );

        ALTER TABLE iam_external_identities
            ADD CONSTRAINT fk_iam_external_identities_user
            FOREIGN KEY (user_id) REFERENCES iam_users(id) ON DELETE CASCADE
            NOT VALID;

        ALTER TABLE iam_external_identities
            VALIDATE CONSTRAINT fk_iam_external_identities_user;
    END IF;
END $$;
